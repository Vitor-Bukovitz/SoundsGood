import UIKit

extension UIImage {
    
    open class var searchIcon: UIImage? {
        return UIImage(named: "search-icon")
    }
    
    open class var menuIcon: UIImage? {
        return UIImage(named: "menu-icon")
    }
    
    open class var playIcon: UIImage? {
        return UIImage(named: "play-icon")
    }
    
    open class var skipIcon: UIImage? {
        return UIImage(named: "skip-icon")
    }
    
    open class var previousIcon: UIImage? {
        return UIImage(named: "previous-icon")
    }
}
